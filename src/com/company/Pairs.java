public class Pairs {

    int key;
    int value;

    public Pairs(int key, int b) {
        this.key = key;
        this.value = b;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
